-- Pulizia iniziale: elimina le tabelle se esistono già (utile per resettare l'ambiente)
DROP TABLE IF EXISTS tag CASCADE;
DROP TABLE IF EXISTS partecipazione CASCADE;
DROP TABLE IF EXISTS partita CASCADE;
DROP TABLE IF EXISTS utente CASCADE;
DROP TABLE IF EXISTS notifiche CASCADE;

-- Creazione tabelle
CREATE TABLE utente (
    ute_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ute_username VARCHAR(50) NOT NULL,
    ute_password VARCHAR(50) NOT NULL,
    ute_immagine VARCHAR(100) NOT NULL
);

CREATE TABLE partita (
    par_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    par_nome VARCHAR(50) NULL,
    par_organizzatore INT NOT NULL,
    par_latitudine FLOAT NOT NULL,
    par_longitudine FLOAT NOT NULL,
    par_data TIMESTAMP NOT NULL, -- Supabase consiglia TIMESTAMPTZ, ma TIMESTAMP va bene per emulare DATETIME
    par_descrizione VARCHAR(100) NULL,
    par_codiice VARCHAR(20),
    FOREIGN KEY (par_organizzatore) REFERENCES utente(ute_id)
);

CREATE TABLE partecipazione (
    prt_partita INT NOT NULL,
    prt_utente INT NOT NULL,
    FOREIGN KEY (prt_partita) REFERENCES partita(par_id),
    FOREIGN KEY (prt_utente) REFERENCES utente(ute_id),
    PRIMARY KEY (prt_partita, prt_utente)
);

CREATE TABLE tag (
    tag_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tag_posizione INT NOT NULL,
    tag_partita INT NOT NULL,
    tag_hash VARCHAR(50) NOT NULL,
    tag_latitudine FLOAT NOT NULL,
    tag_longitudine FLOAT NOT NULL,
    tag_descrizione VARCHAR(100) NOT NULL,
    tag_immagine VARCHAR(100) NOT NULL,
    FOREIGN KEY (tag_partita) REFERENCES partita(par_id)
);

CREATE TABLE NOTIFICHE (
    not_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    not_utente INT NOT NULL,
    not_data timestamp default now(),
    not_messaggio VARCHAR(150),
    not_letto boolean  default false,
    FOREIGN KEY (not_utente) REFERENCES utente(ute_id)
);

