-- 4. Aggiunta tag
-- Sostituito il trucco della CTE manuale con generate_series() nativo di Postgres
WITH Nums AS (
    SELECT generate_series(1, 5) AS n
)
INSERT INTO tag (
    tag_posizione,
    tag_partita,
    tag_hash,
    tag_latitudine,
    tag_longitudine,
    tag_descrizione,
    tag_immagine
)
SELECT 
    n.n AS tag_posizione,
    p.par_id AS tag_partita,
    -- NEWID() diventa gen_random_uuid() in Postgres
    CONCAT('HASH_', p.par_id, '_', n.n, '_', LEFT(CAST(gen_random_uuid() AS VARCHAR(50)), 8)) AS tag_hash,
    -- CHECKSUM non esiste. Usiamo random() che genera un float tra 0.0 e 1.0.
    -- (random() * 0.02 - 0.01) restituisce esattamente un offset tra -0.01 e +0.01
    p.par_latitudine + (random() * 0.02 - 0.01) AS tag_latitudine,
    p.par_longitudine + (random() * 0.02 - 0.01) AS tag_longitudine,
    CONCAT('Tag n.', n.n, ' - Partita ', p.par_id) AS tag_descrizione,
    'upload/default.jpg' AS tag_immagine
FROM 
    partita p
CROSS JOIN 
    Nums n;

